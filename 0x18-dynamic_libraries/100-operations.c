/**
 * add - a function that adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: differences
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies by two integers
 * @a: first integer
 * @b: second integer
 * Return: their product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide an integer by another integer
 * @a: integer
 * @b: divisor
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
	
}

/**
 * mod - finds  modulo of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
